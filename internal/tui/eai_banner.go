// GetBanner returns the appropriate banner based on terminal width
func GetBanner(width int) string {
	titleStyle := lipgloss.NewStyle().
		Foreground(lipgloss.Color(colorAccent)).
		Bold(true)

	subtitleStyle := lipgloss.NewStyle().
		Foreground(lipgloss.Color(colorMuted)).
		Italic(true)

	if width < 100 {
		// Use compact banner for smaller terminals
		return titleStyle.Render(compactBanner)
	}

	// Full banner with monkey for larger terminals
	banner := titleStyle.Render(eaiTextASCII)
	subtitle := subtitleStyle.Render("       intelligent cli agent")

	return lipgloss.JoinVertical(lipgloss.Left, banner, subtitle)
}

// GetMinimalHeader returns a minimal one-line header
func GetMinimalHeader() string {
	return lipgloss.NewStyle().
		Foreground(lipgloss.Color(colorAccent)).
		Bold(true).
		Render("eai")
}
